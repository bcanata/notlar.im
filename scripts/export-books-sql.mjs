#!/usr/bin/env node
/**
 * One-shot: turn the enriched catalogue (src/data/books.json) into seed SQL
 * for the D1 `books` table (db/seed.sql).
 *
 * Slugs are computed with the SAME isbn13 → isbn10 → title-slug rule, in the
 * SAME array order, as the old static build's allBooks() — so every existing
 * /kitaplik/<slug> and /en/library/<slug> URL keeps resolving after the move
 * to D1. Don't "improve" the slug logic here without a redirect plan.
 *
 * cover_key mirrors the old enrich-books.mjs key: "<isbn13|isbn10>.jpg".
 * Those same files get uploaded to R2 by scripts/upload-covers-r2.mjs.
 *
 * Run: node scripts/export-books-sql.mjs   (or: pnpm export-books-sql)
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = join(HERE, '..');
const IN = join(ROOT, 'src', 'data', 'books.json');
const OUT = join(ROOT, 'db', 'seed.sql');

// --- ported verbatim from src/lib/tags.ts + src/lib/books.ts ---
const TURKISH_MAP = {
    ç: 'c', Ç: 'c', ğ: 'g', Ğ: 'g', ı: 'i', İ: 'i',
    ö: 'o', Ö: 'o', ş: 's', Ş: 's', ü: 'u', Ü: 'u',
};
function tagSlug(tag) {
    return tag
        .toLocaleLowerCase('tr')
        .split('')
        .map((ch) => TURKISH_MAP[ch] ?? ch)
        .join('')
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/(^-|-$)/g, '');
}
function rawSlug(b) {
    if (b.isbn13) return b.isbn13;
    if (b.isbn10) return b.isbn10;
    return tagSlug(b.title) || 'kitap';
}

const { books } = JSON.parse(readFileSync(IN, 'utf8'));

// Assign stable, unique slugs in array order (matches allBooks()).
const seen = new Map();
const rows = books.map((b) => {
    let slug = rawSlug(b);
    const n = seen.get(slug) ?? 0;
    seen.set(slug, n + 1);
    if (n > 0) slug = `${slug}-${n + 1}`;
    // book.cover is "/covers/<key>.jpg" or null → cover_key is "<key>.jpg".
    const coverKey = b.cover ? b.cover.replace(/^\/covers\//, '') : null;
    return { ...b, slug, coverKey };
});

const q = (v) => (v === null || v === undefined || v === '' ? 'NULL' : `'${String(v).replace(/'/g, "''")}'`);
const n = (v) => (v === null || v === undefined || v === '' ? 'NULL' : Number(v));

// No explicit BEGIN/COMMIT: D1 executes a --file atomically in its own
// transaction and rejects SQL-level BEGIN TRANSACTION/SAVEPOINT.
const lines = [
    '-- Generated by scripts/export-books-sql.mjs — do not edit by hand.',
    '-- Apply: wrangler d1 execute notlar-library --file=db/seed.sql --remote',
];
for (const b of rows) {
    lines.push(
        'INSERT INTO books (slug, title, author, isbn13, isbn10, publisher, year, pages, description, status, added, cover_key) VALUES (' +
            [
                q(b.slug), q(b.title), q(b.author), q(b.isbn13), q(b.isbn10),
                q(b.publisher), n(b.year), n(b.pages), q(b.description),
                q(b.status), q(b.added), q(b.coverKey),
            ].join(', ') +
            ');',
    );
}
// One physical copy per book, in the original LibraryThing collection, so a
// fresh setup mirrors the migrated state (matches db/migrations/0003_copies.sql).
lines.push(
    "INSERT INTO copies (book_id, bookcase, added) SELECT id, 'Çalışma Odası', added FROM books;",
    '',
);

writeFileSync(OUT, lines.join('\n'));
console.log(`✓ Wrote ${OUT} — ${rows.length} books, ${rows.filter((r) => r.coverKey).length} with covers.`);
